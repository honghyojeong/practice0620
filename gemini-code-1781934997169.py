SYNONYMS = {
    "혼자": ["혼자", "홀로", "독립된", "스스로", "타인 없이", "단독"],
    "함께": ["함께", "같이", "모임", "도서관", "커피숍", "다른 사람"],
    "정지": ["정지", "멈춤", "고여 있는", "이동하지 않는", "움직이지 않는"],
    "음미": ["음미", "천천히", "여유", "깊이 있게", "살펴보는"]
}

def check_synonyms(text, key):
    return any(word in text for word in SYNONYMS.get(key, [key]))

